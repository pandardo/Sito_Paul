                        <img src="../images/work.jpeg" id="immagine_center"/><h3>My Work</h3>
                        N’dri Paul Kouadio is a ceramic artist from Ivory Coast presently operating in Toronto, Canada, where he moved after having studied and worked as a Ceramics teacher in Japan for 8 years.
                        <br/><br/>
                        He makes ceramic sculptures inspired from African traditional art and natural environment. 
                        <br/><br/>
                        His main techniques are moulding and slap building, as well as throwing and hand forming. 
                        <br/><br/>
                        He makes most of his glazes from natural materials, like wood ash and lemon ocher. 
                        <br/><br/>
                        Recently he approaches to the use of cashew and other colorants, inspiring from Japanese tradition of lacquer wares.
