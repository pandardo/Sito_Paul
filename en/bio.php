                        <img src="../images/biography.jpeg" id="immagine_center"/><h3>Biography</h3><br/><br/>
                        Kouadio N’dri Paul was born in 1975 in Divo, Ivory Coast.
                        <br/><br/>
                        He spent his childhood in a small countryside village called Djétrankro, where his parents are still working as cocoa 
                        farmers. As long as he can remember, he has been fascinated with the clay, especially its good smell spreading after 
                        rain, that he loved so much during his childhood. 
                        <br/><br/>
                        At 6, he left his family and the countryside for Abidjan, the biggest city of Ivory Coast, to study at the elementary 
                        school. As a countryside boy, he really was shocked by the difference between the way of life in Abidjan and Djétrankro. 
                        A long time later, he realized that that shock was positive, because it permitted him to make the difference between 
                        the countryside’s life, which is based on African traditional culture, and the life in an African city, which is 
                        somewhat a copy of the western culture. 
                        <br/><br/>
                        His departure to Abidjan did not separate him from his tribe’s traditional culture, because, until his graduation from 
                        the senior high school, he spent all his holidays and summer vacations with his parents and his brothers and sisters. 
                        Going back to Djétrankro was exciting, because it was an opportunity to learn from his parents and the other adults 
                        about his ancestors’ culture.
                        <br/><br/>
                        After his graduation from senior high school, he entered the “Ecole Nationale des Beaux arts” of Abidjan. At Art school, 
                        he rediscovered the traditional culture of the countryside, but this time it was through books and art works. He 
                        understood that art was the best field to express himself.
                        Remembering the good smell he loved during his childhood, he fell in love with clay at his first touch. Therefore, 
                        he decided to enter the ceramic art specialty, after two years of pre-specialty.
                        <br/><br/>
                        In 2001, he graduated from the “Ecole Nationale des Beaux arts”, and the same year, he entered the “Centre de Formations 
                        Pédagogiques et Artistiques” which is a fine art teachers’ training school. After this training, he decided to carry on 
                        his studies in ceramic arts, because his dream was to teach ceramic in an art school. 
                        <br/><br/>
                        Thanks to a Japanese teacher working at the Ceramics Department of the “Ecole Nationale des Beaux arts” and Japanese 
                        friends living in Abidjan, he knew Japanese contemporary ceramic art, and discovered the long tradition that this 
                        country has in this art. 
                        <br/><br/>
                        So, in 2002, he applied for, and received the Monbukagakusho, a scholarship sponsored by Japanese Government, to study 
                        in Kyoto.
                        In April 2004, after studying Japanese language for one year at Kyoto University, he entered the Master Course of Kyoto 
                        University of Art and Design, where he graduated two years later. 
                        <br/><br/>
                        In April 2006, he passed the Doctoral Courses entrance examination at the same university, and achieved his PhD in Art 
                        and Art History in March 2009.
                        <br/><br/>
                        From 2009 to 2010, he pursued his research on fertility dolls as a Visiting Researcher at the National Museum of 
                        Ethnology (Minpaku) in Osaka.
                        <br/><br/>
                        From 2010 to 2011 he worked as a Ceramics teacher at Kashogama Ceramics School in Kyoto, where he enriched his ceramic 
                        skills and abilities.
                        <br/><br/>
                        Now he lives in Toronto, Canada, where he is working on his ceramics at the Miles Nadal Jewish Community Center, and he 
                        is organizing his first solo exhibition in Toronto. 
